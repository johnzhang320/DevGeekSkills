	/*-----------------Insert Country and their cities to  Mongodb --------------*/
	
	public static void InsertCountryCitiesToMongo(Integer A[]) {
		
		DBCollection countryCollection = CollectionSingleton.getInstance().getCollectionCountry();
		countryCollection.drop();
		for (Integer i=0;i<A.length;i++) { 
		
			String jsonString = getCountryCityJSONString(A[i]);		
	 	
			DBObject dbObject = (DBObject)JSON.parse(jsonString);				
		
			countryCollection.insert(dbObject);
		}
 	  
	}
	
	/*-----------------Insert Country and their cities to  Mongodb --------------*/
	
